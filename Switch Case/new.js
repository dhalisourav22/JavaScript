var number = parseInt(prompt("Enter the digit : "));
switch(number){
    case "0":{
        document.write("Zero");
        break;
    }
    case "1" :{
        document.write("One");
        break;
    } 
    case "2":{
        document.write("Two");
        break;
    }
    case "3" :{
        document.write("Three");
        break;
    } 
    default : {
        document.write("This is not we want.");
    }
}